require('hpc/hmap')

# @return A hash of VIP hash for the current host
Puppet::Parser::Functions::newfunction(
  :hpc_hmap,
  :type  => :rvalue,
  :arity => -1,
  :doc   =>
    "Transform a hash of values into a hash of hash"
) do |args|

  hash = args[0]
  key  = args[1]
  result = hpc_hmap(hash, key)

  begin
    result
  rescue Puppet::ParseError => internal_error
    if internal_error.original.nil?
      raise internal_error
    else
      raise internal_error.original
    end
  end
end
